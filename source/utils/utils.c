bool arr_eq(uint8_t* arr1, uint8_t arr1_len, uint8_t * arr2, uint8_t arr2_len){
	bool r = false;
	uint8_t i;
	if (arr1_len == arr2_len){
		for (i = 0; i < arr1_len; i++){
			if (*(arr1 + i) == *(arr2 + i)){
				r = true;
			} else {
				r = false;
				break;
			}
		}
	}
	return r;
}

void copy_arr(uint8_t* og, uint8_t* copy, uint8_t len){
	uint8_t i;
	for (i = 0; i < len; i++){
		*(copy + i) = *(og + i);
	}
}

